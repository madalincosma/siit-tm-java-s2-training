package ro.tm.siit.classroom.w4d2.carperson0;

/**
 * CarPerson data class keeps data/state to help model a person with a car.
 * 
 * @author mco
 *
 */
public class CarPersonData {

	/**
	 * keeps fuel level
	 */
	public int fuelLevel;

	/**
	 * flag that sets car open
	 */
	public boolean open;

	/**
	 * the key that opens the car
	 */
	public String realKey;

	/**
	 * the name of the car; used as identifier also
	 */
	public String name;

	/**
	 * the cra key owned by the person
	 */
	public String carKey;

}
